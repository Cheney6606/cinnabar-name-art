function estimateStrokeCount(char) {
  return strokeCountMap[char] || 6; // 默认笔画更宽松
}

export function isNameSafe(name, context) {
  if (!name || !name.characters) {
    return { safe: true, reason: '' };
  }

  const containsBannedWord = bannedWords.some(banned => 
    name.characters.includes(banned) || 
    name.pinyin.includes(banned)
  );

  if (containsBannedWord) {
    return {
      safe: false,
      reason: 'This name contains inappropriate characters. Please try again.'
    };
  }

  if (context?.goal === 'tattoo') {
    const chars = name.characters.split('');
    
    // 检查每个字符不超过15画
    for (const char of chars) {
      const strokes = estimateStrokeCount(char);
      if (strokes > 15) {
        return {
          safe: false,
          reason: 'We generate tattoo-safe names with characters limited to 15 strokes or fewer. Please try again.'
        };
      }
    }
  }

  return {
    safe: true,
    reason: ''
  };
}
